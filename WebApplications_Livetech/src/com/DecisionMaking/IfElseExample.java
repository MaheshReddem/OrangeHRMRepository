package com.DecisionMaking;

public class IfElseExample {
	
	public static void main(String[] args) {
		
		int var1=20;
		int var2=30;
		
		if(var1>var2)
		{
			System.out.println(" The value of variable var1 is greater than value of variable var2 ");
		}
		else
		{
			System.out.println(" The value of variable var2 is less than value of variable var1 ");
		}
				
	}

}
